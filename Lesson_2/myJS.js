function changeColor() {
    document.getElementById("name").style.color="red";
    document.getElementById("email").style.color="red";
    document.getElementById("major").style.color="red";
}
changeColor();
function changeFont() {
    document.getElementById("name").style.fontFamily = "Verdana";
    document.getElementById("email").style.fontFamily = "Verdana";
    document.getElementById("major").style.fontFamily = "Verdana";
}
changeFont();
